#include "libft.h"

int		ft_inof(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	if (s[i] == '\0' && c == '\0')
		return (i);
	return (0);
}
