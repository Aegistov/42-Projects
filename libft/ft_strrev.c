#include "libft.h"

char	*ft_strrev(char *dst, char *src)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (src[j] != '\0')
		j++;
	j--;
	while (j >= 0)
	{
		dst[i] = src[j];
		j--;
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
